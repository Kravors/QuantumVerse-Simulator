def train_epoch(model: nn.Module, dataloader: DataLoader, 
                optimizer: optim.Optimizer, criterion: nn.Module,
                lambda_symplectic: float = 0.1, device: torch.device = device) -> float:
    """Train for one epoch."""
    model.train()
    total_loss = 0.0
    
    for batch_idx, (inputs, targets) in enumerate(dataloader):
        inputs, targets = inputs.to(device), targets.to(device)
        
        optimizer.zero_grad()
        predictions = model(inputs)
        
        # Main MSE loss
        mse_loss = criterion(predictions, targets)
        
        # Symplectic regularization (optional)
        # reg_loss = symplectic_loss(predictions, targets, inputs, lambda_symplectic)
        reg_loss = torch.tensor(0.0, device=device)
        
        loss = mse_loss + lambda_symplectic * reg_loss
        loss.backward()
        
        # Gradient clipping (stabilize training)
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        
        optimizer.step()
        
        total_loss += loss.item()
    
    return total_loss / len(dataloader)

def validate(model: nn.Module, dataloader: DataLoader, 
             criterion: nn.Module, device: torch.device = device) -> float:
    """Validate on held-out set."""
    model.eval()
    total_loss = 0.0
    
    with torch.no_grad():
        for inputs, targets in dataloader:
            inputs, targets = inputs.to(device), targets.to(device)
            predictions = model(inputs)
            loss = criterion(predictions, targets)
            total_loss += loss.item()
    
    return total_loss / len(dataloader)

def export_to_onnx(model: nn.Module, sample_input: torch.Tensor, onnx_path: Path):
    """Export trained model to ONNX format for C++ deployment."""
    model.eval()
    
    # Ensure sample_input is on CPU for ONNX export
    sample_input = sample_input.cpu()
    
    # Fix Windows Unicode issue: redirect stdout to avoid cp1252 encoding errors
    # from torch.onnx internal verbose prints (checkmarks, etc.)
    import sys
    import io
    old_stdout = sys.stdout
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')
    
    try:
        torch.onnx.export(
            model,
            sample_input,
            str(onnx_path),
            export_params=True,
            opset_version=14,  # ONNX opset version
            do_constant_folding=True,
            input_names=['input'],
            output_names=['final_event'],
            dynamic_axes={
                'input': {0: 'batch_size'},
                'final_event': {0: 'batch_size'}
            }
        )
    finally:
        sys.stdout = old_stdout
    
    print(f"Model exported to ONNX: {onnx_path}")

def train(dataset_path: Path, output_dir: Path, resume_path: Optional[Path] = None):
    """Main training loop."""
    
    # Create output directory
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Hyperparameters (stable configuration - proven to work)
    hparams = {
        'hidden_dim': 256,
        'num_layers': 3,
        'learning_rate': 1e-3,
        'batch_size': 128,  # Stable batch size (was 1024 - causing crashes)
        'num_epochs': 100,
        'weight_decay': 1e-5,
        'lambda_symplectic': 0.0  # disable for now
    }
    
    # Save hyperparameters
    with open(output_dir / 'hparams.json', 'w') as f:
        json.dump(hparams, f, indent=2)
    
    # Create datasets
    print("Loading datasets...")
    train_dataset = GeodesicDataset(dataset_path, split='train')
    val_dataset = GeodesicDataset(dataset_path, split='val')
    
    # DataLoader: stable configuration (num_workers=0 is reliable)
    train_loader = DataLoader(train_dataset, batch_size=hparams['batch_size'],
                              shuffle=True, num_workers=0, pin_memory=True)
    val_loader = DataLoader(val_dataset, batch_size=hparams['batch_size'],
                            shuffle=False, num_workers=0, pin_memory=True)
    
    print(f"Train samples: {len(train_dataset)}, Val samples: {len(val_dataset)}")
    
    # Determine input dimension from first sample
    sample_input, _ = train_dataset[0]
    input_dim = sample_input.shape[0]
    output_dim = 4  # final event
    
    print(f"Input dim: {input_dim}, Output dim: {output_dim}")
    
    # Create model
    model = GeodesicMLP(
        input_dim=input_dim,
        hidden_dim=hparams['hidden_dim'],
        output_dim=output_dim,
        num_layers=hparams['num_layers']
    ).to(device)
    
    # Resume from checkpoint if provided
    start_epoch = 0
    if resume_path:
        checkpoint = torch.load(resume_path, map_location=device)
        model.load_state_dict(checkpoint['model_state_dict'])
        start_epoch = checkpoint['epoch'] + 1
        print(f"Resumed from epoch {start_epoch}")
    
    # Loss and optimizer
    criterion = nn.MSELoss()
    optimizer = optim.Adam(model.parameters(), 
                          lr=hparams['learning_rate'],
                          weight_decay=hparams['weight_decay'])
    
    # Learning rate scheduler (reduce on plateau)
    scheduler = optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min',
                                                     factor=0.5, patience=5)
    
    # Training loop
    print("Starting training...")
    best_val_loss = float('inf')
    
    for epoch in range(start_epoch, hparams['num_epochs']):
        start_time = time.time()
        
        train_loss = train_epoch(model, train_loader, optimizer, criterion,
                                 hparams['lambda_symplectic'], device)
        val_loss = validate(model, val_loader, criterion, device)
        
        scheduler.step(val_loss)
        
        epoch_time = time.time() - start_time
        
        print(f"Epoch {epoch+1}/{hparams['num_epochs']} | "
              f"Train: {train_loss:.6f} | Val: {val_loss:.6f} | "
              f"Time: {epoch_time:.1f}s")
        
        # Save checkpoint
        checkpoint = {
            'epoch': epoch,
            'model_state_dict': model.state_dict(),
            'optimizer_state_dict': optimizer.state_dict(),
            'train_loss': train_loss,
            'val_loss': val_loss
        }
        torch.save(checkpoint, output_dir / f'checkpoint_epoch{epoch+1}.pth')
        
        # Save best model
        if val_loss < best_val_loss:
            best_val_loss = val_loss
            torch.save(checkpoint, output_dir / 'best_model.pth')
            print(f"  -> New best model (val_loss={val_loss:.6f})")
    
    # Final export to ONNX
    print("Training complete. Exporting to ONNX...")
    best_checkpoint = torch.load(output_dir / 'best_model.pth', map_location=device)
    model.load_state_dict(best_checkpoint['model_state_dict'])
    
    # Create sample input for ONNX export
    sample_batch = next(iter(val_loader))[0][:1].to(device)  # 1 sample
    onnx_path = output_dir / 'geodesic_ode.onnx'
    export_to_onnx(model, sample_batch, onnx_path)
    
    # Save metadata
    metadata = {
        'train_loss': float(best_checkpoint['train_loss']),
        'val_error': float(best_checkpoint['val_loss']),
        'num_epochs': hparams['num_epochs'],
        'input_dim': int(input_dim),
        'output_dim': output_dim,
        'hidden_dim': hparams['hidden_dim'],
        'num_layers': hparams['num_layers'],
        'date_trained': datetime.now().isoformat(),
        'dataset': str(dataset_path)
    }
    with open(output_dir / 'metadata.json', 'w') as f:
        json.dump(metadata, f, indent=2)
    
    print(f"Model and metadata saved to {output_dir}")
    return str(onnx_path)

