# Device selection
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"Using device: {device}")

class GeodesicDataset(Dataset):
    """PyTorch dataset for geodesic samples with multi-worker support."""
    
    def __init__(self, h5_path: Path, split: str = 'train', train_frac: float = 0.8):
        """
        Load dataset from HDF5 and split into train/val/test.
        
        Args:
            h5_path: Path to HDF5 file generated by datagen.py
            split: 'train', 'val', or 'test'
            train_frac: Fraction of data for training (rest split equally val/test)
        """
        super().__init__()
        self.h5_path = str(h5_path)  # convert to string for multiprocessing
        self.split = split
        self.train_frac = train_frac
        
        # Load indices only (not data) — this is done in main process
        with h5py.File(h5_path, 'r') as f:
            N = len(f['final_event'])
            indices = np.arange(N)
            np.random.seed(42)  # reproducible split
            np.random.shuffle(indices)
            
            n_train = int(N * train_frac)
            n_val = (N - n_train) // 2
            n_test = N - n_train - n_val
            
            if split == 'train':
                self.indices = indices[:n_train]
            elif split == 'val':
                self.indices = indices[n_train:n_train+n_val]
            else:  # test
                self.indices = indices[n_train+n_val:]
            
            self.N = len(self.indices)
        
        # Each worker will open its own HDF5 handle
        self.h5_file = None
    
    def __len__(self):
        return self.N
    
    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        """Get (input, output) pair. Thread-safe for multi-worker DataLoader."""
        # Open HDF5 file on first access in this worker (fork-safe)
        if self.h5_file is None:
            self.h5_file = h5py.File(self.h5_path, 'r')
        
        i = self.indices[idx]
        
        # Load arrays
        initial_event = self.h5_file['initial_event'][i]      # (4,)
        initial_velocity = self.h5_file['initial_velocity'][i]  # (4,)
        metric_params = self.h5_file['metric_params'][i]      # (N_params,)
        tau = self.h5_file['tau'][i]                          # scalar
        final_event = self.h5_file['final_event'][i]          # (4,)
        
        # Concatenate input: [event(4), velocity(4), tau(1), metric_params(N)]
        input_vec = np.concatenate([initial_event, initial_velocity, [tau], metric_params])
        input_tensor = torch.tensor(input_vec, dtype=torch.float32)
        
        # Output: final_event (4,)
        output_tensor = torch.tensor(final_event, dtype=torch.float32)
        
        return input_tensor, output_tensor

class GeodesicMLP(nn.Module):
    """Multi-layer perceptron for geodesic prediction."""
    
    def __init__(self, input_dim: int = 9, hidden_dim: int = 256, output_dim: int = 4, num_layers: int = 3):
        """
        Initialize MLP.
        
        Args:
            input_dim: Input dimension (4 event + 4 velocity + 1 tau + N params)
            hidden_dim: Hidden layer width (default 256)
            output_dim: Output dimension (4 event coords)
            num_layers: Number of hidden layers (default 3 → 256-256-256)
        """
        super().__init__()
        
        layers = []
        in_dim = input_dim
        
        for _ in range(num_layers):
            layers.append(nn.Linear(in_dim, hidden_dim))
            layers.append(nn.ReLU())
            layers.append(nn.BatchNorm1d(hidden_dim))  # stabilize training
            in_dim = hidden_dim
        
        # Output layer (no activation)
        layers.append(nn.Linear(in_dim, output_dim))
        
        self.network = nn.Sequential(*layers)
        
        # Initialize weights (Kaiming for ReLU)
        self.apply(self._init_weights)
    
    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            nn.init.kaiming_normal_(m.weight, mode='fan_in', nonlinearity='relu')
            if m.bias is not None:
                nn.init.zeros_(m.bias)
    
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """Forward pass: x shape (batch, input_dim) → (batch, output_dim)."""
        return self.network(x)

